// Question 1a:
console.log("1a: Numbers up to ten (using for loop)");
for (let i = 0; i <= 10; i++) {
    console.log(i);
}

// Question 1b:
console.log("1b: Numbers up to ten (using while loop)");
let i = 0;
while (i <= 10) {
    console.log(i);
    i++
}

// Question 2:
console.log("2: Even numbers to 20 (using for loop)");
for (let i = 0; i <= 20; i += 2) {
    console.log(i);
}

// Question 3:
console.log("3: Odd numbers to 20 (using while loop)");
let j = 1;
while (j <= 20) {
    console.log(j);
    j += 2;
}

// Question 4:
console.log("4: Back to 0 (using for loop)");
for (let i = 10; i >= 0; i--) {
    console.log(i);
}
